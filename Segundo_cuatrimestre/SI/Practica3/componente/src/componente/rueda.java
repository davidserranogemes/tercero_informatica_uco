 public void subirVolumen(){
        if(volumen<4){
            volumen++;
        }
        charge_Image();
    }
    public void bajarVolumen(){
        if(volumen>0){
            volumen--;
        }
        charge_Image();

    }
    void charge_Image(){
        ImageIcon icon;
        URL resource = null;
        switch(volumen){
            case 0:
                resource = getClass().getResource("/componente/images/volumen_izq.png");
                jTextField1.setText("0 %");
                break;
            case 1:
                resource = getClass().getResource("/componente/images/volumen_mid_izq.png");
                jTextField1.setText("25 %");
                break;
            case 2:
                resource = getClass().getResource("/componente/images/volumen_arriba.png");
                jTextField1.setText("50 %");
                break;
            case 3:
                resource = getClass().getResource("/componente/images/volumen_mid_der.png");
                jTextField1.setText("75 %");
                break;
            case 4:
                resource = getClass().getResource("/componente/images/volumen_der.png");
                jTextField1.setText("100 %");
                break;
            default:
                System.out.println("Error imposible, Â¿que has hecho?");
        }
        icon = new ImageIcon(resource);
        jLabel2.setIcon(icon);
    }
    
    
