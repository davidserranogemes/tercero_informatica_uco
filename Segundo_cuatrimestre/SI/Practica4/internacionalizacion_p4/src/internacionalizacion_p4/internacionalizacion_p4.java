    private void changeLanguaje(String Language){
	if(!Language.equals(Lang)){
	   Locale currentLocale;
	   ResourceBundle bundle;
	   String LastLang=Lang;
	   switch(Language){
		case"ES":
		   Lang="ES";
		   currentLocale = new Locale("es","ES");
		   this.setTitle("Internacionalización");
		   break;
		case"EN":
		   Lang="EN";
		   currentLocale = new Locale("en","GB");
		   this.setTitle("Internacionalization");
		   break;
		default:
		   Lang="EN";
		   this.setTitle("Internacionalization");
		   currentLocale = new Locale("en","GB");
	   }
	   bundle= ResourceBundle.getBundle("internacionalizacion_p4/Bundle",currentLocale);

	   jLabel1.setText(bundle.getString("internacionalizacion_p4.jLabel1.text"));
	   changeNumber(LastLang);
	   
	   jLabel2.setText(bundle.getString("internacionalizacion_p4.jLabel2.text"));
	   changeCurrency(LastLang);
	   
	   jLabel3.setText(bundle.getString("internacionalizacion_p4.jLabel3.text"));
	   changeDate(LastLang);
	   
	}
    }
    
