			    //System.out.println(aux);
			    currency='€';
			    if(aux.indexOf(currency)<0){
				//lenght--;
				aux=aux+"€";
			    }
			    lenght=aux.length();
			    charArrayReturn=aux.toCharArray();
			    charArray = aux.toCharArray();
       			    charArrayReturn[0]='£';

