    private void charge_items_combobox_permisos(){
       
            boolean exist=false;
        
        for( int i=0; i<partidos_table.getRowCount();i++){
            boolean check = (boolean) partidos_table.getValueAt(i,5);
            if(check){
                add_partido_to_combo(education_jComboBox,i);
                add_partido_to_combo(sanidad_jComboBox,i);
                add_partido_to_combo(seguridad_jComboBox,i);
                add_partido_to_combo(politico_jComboBox,i);
                add_partido_to_combo(justicia_jComboBox,i);
                add_partido_to_combo(internacional_jComboBox,i);
                add_partido_to_combo(presupuestos_jComboBox,i);
                add_partido_to_combo(economia_jComboBox,i);
                                
            }else{
               delete_partido_from_combo(education_jComboBox,i);
               delete_partido_from_combo(sanidad_jComboBox,i);
               delete_partido_from_combo(seguridad_jComboBox,i);
               delete_partido_from_combo(politico_jComboBox,i);
               delete_partido_from_combo(justicia_jComboBox,i);
               delete_partido_from_combo(internacional_jComboBox,i);
               delete_partido_from_combo(presupuestos_jComboBox,i);
               delete_partido_from_combo(economia_jComboBox,i);

                
            }
        }
        
        
    }
    private void add_partido_to_combo(javax.swing.JComboBox combo,int i){
        boolean exist=false;
        for (int j=0;j<combo.getItemCount();j++){
            if(combo.getItemAt(j)==partidos_table.getValueAt(i,1)){
                exist=true;
            }
        }
        if(!exist){
           combo.addItem(partidos_table.getValueAt(i,1));
        }
        exist=false;
     }
    
    private void delete_partido_from_combo(javax.swing.JComboBox combo,int i){
        for (int j=0;j<combo.getItemCount();j++){
            if(combo.getItemAt(j)==partidos_table.getValueAt(i,1)){
                if(combo.getItemAt(j)==combo.getSelectedItem()){
                    combo.setSelectedItem("Ninguno");
                }
                combo.removeItemAt(j);
            }
        }
    }
    
