    private void return_last_page_buttonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_return_last_page_buttonMouseClicked

        if(lastPanel.size()>1){ 
            javax.swing.JPanel auxPanel;
            auxPanel = (JPanel) lastPanel.get(lastPanel.size()-1);
            
            content_panel.removeAll();
            content_panel.add(auxPanel);
            content_panel.repaint();
            content_panel.revalidate();
            
            currentPanel=auxPanel;
            
            lastPanel.remove(lastPanel.size()-1);
        }else{
            if(lastPanel.size()==1){
           
                 content_panel.removeAll();
                 content_panel.add(welcome_panel);
                 content_panel.repaint();
                 content_panel.revalidate();
            
                 currentPanel=welcome_panel;
            
                lastPanel.remove(lastPanel.size()-1);
            }
        }
    }//GEN-LAST:event_return_last_page_buttonMouseClicked

